let generated: Record<string, Partial<Messages>> = generatedLocales as Record<string, Partial<Messages>>;
try {
  const cached = JSON.parse(localStorage.getItem("narucare-generated-locales") || "{}") as Record<string, Partial<Messages>>;
  for (const [locale, messages] of Object.entries(cached)) generated[locale] = { ...(generated[locale] || {}), ...messages };
} catch {
  // The bundled static locale packs remain available when browser storage is unavailable.
}

const builtIn: Record<string, Partial<Messages>> = { "zh-CN": zh, en, ko, ja };

interface I18nContextValue {
  locale: string;
  option: LocaleOption;
  setLocale: (locale: string) => void;
  t: (key: keyof Messages, vars?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState(() => localStorage.getItem("narucare-locale") || "zh-CN");
  const option = localeOptions.find((item) => item.code === locale) || localeOptions[0];
  const dictionary = useMemo<Messages>(() => ({ ...en, ...(generated[locale] || {}), ...(builtIn[locale] || {}) }), [locale]);

  const setLocale = useCallback((next: string) => {
    const nextOption = localeOptions.find((item) => item.code === next) || localeOptions[0];
    localStorage.setItem("narucare-locale", nextOption.code);
    document.documentElement.lang = nextOption.code;
    document.documentElement.dir = "ltr";
    document.documentElement.dataset.localeDirection = nextOption.direction || "ltr";
    setLocaleState(nextOption.code);
  }, []);

  useEffect(() => {
    document.documentElement.lang = option.code;
    document.documentElement.dir = "ltr";
    document.documentElement.dataset.localeDirection = option.direction || "ltr";
  }, [option]);

  const t = useCallback((key: keyof Messages, vars?: Record<string, string | number>) => {
    let value = dictionary[key] || en[key] || String(key);
    if (vars) {
      for (const [name, replacement] of Object.entries(vars)) {
        value = value.replaceAll(`{${name}}`, String(replacement));
      }
    }
    return value;
  }, [dictionary]);

  return <I18nContext.Provider value={{ locale, option, setLocale, t }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider");
  return value;
}

